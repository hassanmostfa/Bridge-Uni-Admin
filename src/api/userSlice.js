import { createApi, fetchBaseQuery } from "@reduxjs/toolkit/query/react";

// Define your base URL
const baseUrl = "https://back.bridgeuni.com/api"; // Replace with your actual base URL

// Function to create request
const createRequest = (url) => ({ url });

// Create the API slice using RTK Query
export const apiService = createApi({
  reducerPath: "apiService",
  baseQuery: fetchBaseQuery({
    baseUrl,
    prepareHeaders: (headers) => {
      // Get the token from localStorage (or Redux state)
      const token = localStorage.getItem("token");

      // If a token exists, add it to the headers
      if (token) {
        headers.set("authorization", `${token}`);
      }

      return headers;
    },
  }),

  endpoints: (builder) => ({
    // Example login request (modify this as per your API endpoint)
    loginUser: builder.mutation({
      query: (userCredentials) => ({
        url: "/admin/login",
        method: "POST",
        body: userCredentials, // Sending user credentials (email and password)
      }),
    }),
    logoutUser: builder.mutation({
      query: () => ({
        url: "/admin/logout",
        method: "POST",
      }),
    }),
    getAdmins: builder.query({
      query: () => ({
        url: '/admin/admin',
      }),
    }),
    // You can add more endpoints here as needed
    getUserProfile: builder.query({
      query: (userId) => createRequest(`/admin/admin-user/${userId}`),
    }),
    createUser: builder.mutation({
      query: (user) => ({
        url: "/admin",
        method: "POST",
        body: user,
      }),
    }),

    updateUser: builder.mutation({
      query: ({id, user}) => ({
        url: `/admin/${id}`,
        method: "PUT",
        body: user,
      }),
    }),

    deleteUser: builder.mutation({
      query: (id) => ({
        url: `/admin/${id}`,
        method: "DELETE",
      }),
    })
  }),
});

// Export hooks generated by the API service
export const { useLoginUserMutation, useLogoutUserMutation, useGetUserProfileQuery, useGetAdminsQuery , useCreateUserMutation, useUpdateUserMutation, useDeleteUserMutation} = apiService;
